''' whileLoop1.py
print the numbers 1 to 10 on one line'''

i = 1 #initialisation is first time we give a value
while (1 <= 10):
   print(i, end=" ") #space after vale not new line
   i = i + 1 #it makes it so it loops ten times instead of infinitely
print() # adding a newline after all valuesd are printed    